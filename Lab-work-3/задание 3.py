def count_letters(text):
    # Создаем пустой словарь для подсчета букв
    letter_count = {}

    # Проходим по каждому символу в тексте
    for char in text:
        # Преобразуем символ в нижний регистр
        lower_char = char.lower()

        # Проверяем, является ли символ буквой
        if lower_char.isalpha():
            # Если буква уже есть в словаре, увеличиваем счетчик
            if lower_char in letter_count:
                letter_count[lower_char] += 1
            else:
                # Если буквы нет в словаре, добавляем ее с начальным значением 1
                letter_count[lower_char] = 1

    return letter_count


def calculate_frequency(letter_count):
    # Общее количество букв
    total_letters = sum(letter_count.values())

    # Создаем словарь для хранения частот
    frequency_dict = {}

    # Рассчитываем частоту для каждой буквы
    for letter, count in letter_count.items():
        frequency = count / total_letters
        frequency_dict[letter] = frequency

    return frequency_dict


main_str = """
У лукоморья дуб зелёный;
Златая цепь на дубе том:
И днём и ночью кот учёный
Всё ходит по цепи кругом;
Идёт направо — песнь заводит,
Налево — сказку говорит.
Там чудеса: там леший бродит,
Русалка на ветвях сидит;
Там на неведомых дорожках
Следы невиданных зверей;
Избушка там на курьих ножках
Стоит без окон, без дверей;
Там лес и дол видений полны;
Там о заре прихлынут волны
На брег песчаный и пустой,
И тридцать витязей прекрасных
Чредой из вод выходят ясных,
И с ними дядька их морской;
Там королевич мимоходом
Пленяет грозного царя;
Там в облаках перед народом
Через леса, через моря
Колдун несёт богатыря;
В темнице там царевна тужит,
А бурый волк ей верно служит;
Там ступа с Бабою Ягой
Идёт, бредёт сама собой,
Там царь Кащей над златом чахнет;
Там русский дух… там Русью пахнет!
И там я был, и мёд я пил;
У моря видел дуб зелёный;
Под ним сидел, и кот учёный
Свои мне сказки говорил.
"""

# Подсчет количества каждой буквы
letter_count = count_letters(main_str)

# Вычисление частоты каждой буквы
frequency_dict = calculate_frequency(letter_count)

# Заданный порядок букв
ordered_letters = [
    'у', 'л', 'к', 'о', 'м', 'р', 'ь', 'я', 'д', 'б', 'з', 'е', 'ё', 'н', 'ы', 'й',
    'а', 'т', 'ц', 'п', 'и', 'ч', 'ю', 'в', 'с', 'х', 'г', 'ш', 'ж', 'щ'
]

# Распечатываем результаты в столбик с округлением до двух знаков после запятой
for letter in ordered_letters:
    frequency = frequency_dict.get(letter, 0)
    print(f"{letter}: {frequency:.2f}")
